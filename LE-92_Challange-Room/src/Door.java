public class Door {
    private String colorOfDoor;
    private String materialOfDoor;

    public Door(String colorOfDoor, String materialOfDoor) {
        this.colorOfDoor = colorOfDoor;
        this.materialOfDoor = materialOfDoor;
    }

    public String getColorOfDoor() {
        return colorOfDoor;
    }

    public String getMaterialOfDoor() {
        return materialOfDoor;
    }
}
